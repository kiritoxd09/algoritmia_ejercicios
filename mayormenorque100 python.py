#titulo: numero mayor o menor que 100

#entrada: se ingresa un numero como variable

#proceso: se calcula el numero ingresado teniendo en cuenta que si es por encima de 100 es mayor y por debajo de 100 como menor

#salida: se determina si el numero es mayor que 100 o si es menor que 100

numero1 =float(input("ingrese un numero:"))
if numero1>= 100:
    print("el numero es mayor que 100")
else: 
    print("el numero es menor que 100")