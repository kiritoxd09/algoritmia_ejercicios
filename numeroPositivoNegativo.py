#titulo: numero positivo y numero negativo

#entrada: ingresar un numero como variable

#proceso: el numero se calculara si es mayor a cero como positivo y si esta por debajo de cero como negativo

#salida: determinar si el numero ingresado es positivo o negativo

numero1 =float(input("ingrese un numero:"))
if numero1>=0:
    print ("el numero es positivo")
else:
    print ("el numero es negativo")